    public int randomRoll() {
        int[] tempArray = new int[4];
        int total;
        int diceRole;
        
        for(int j=0; j<tempArray.length; j++){
                diceRole = 1 +(int) (Math.random() * 6);
                tempArray[j] = diceRole;
            }
        Arrays.sort(tempArray);
        total = tempArray[1] + tempArray[2] + tempArray[3];
        return total;
    }
    
